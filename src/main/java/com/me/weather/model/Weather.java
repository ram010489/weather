package com.me.weather.model;

public class Weather {

	private String Location;
	
	private String Time;
	
	private String Wind;
	
	private String Visibility;
	
	private String SkyConditions;
	
	private String Temperature;
	
	private String DewPoint;
	
	private String RelativeHumidity;
	
	private String Status;

	/**
	 * @return the location
	 */
	public String getLocation() {
		return Location;
	}

	/**
	 * @param location the location to set
	 */
	public void setLocation(String location) {
		Location = location;
	}

	/**
	 * @return the time
	 */
	public String getTime() {
		return Time;
	}

	/**
	 * @param time the time to set
	 */
	public void setTime(String time) {
		Time = time;
	}

	/**
	 * @return the wind
	 */
	public String getWind() {
		return Wind;
	}

	/**
	 * @param wind the wind to set
	 */
	public void setWind(String wind) {
		Wind = wind;
	}

	/**
	 * @return the visibility
	 */
	public String getVisibility() {
		return Visibility;
	}

	/**
	 * @param visibility the visibility to set
	 */
	public void setVisibility(String visibility) {
		Visibility = visibility;
	}

	/**
	 * @return the skyConditions
	 */
	public String getSkyConditions() {
		return SkyConditions;
	}

	/**
	 * @param skyConditions the skyConditions to set
	 */
	public void setSkyConditions(String skyConditions) {
		SkyConditions = skyConditions;
	}

	/**
	 * @return the temperature
	 */
	public String getTemperature() {
		return Temperature;
	}

	/**
	 * @param temperature the temperature to set
	 */
	public void setTemperature(String temperature) {
		Temperature = temperature;
	}

	/**
	 * @return the dewPoint
	 */
	public String getDewPoint() {
		return DewPoint;
	}

	/**
	 * @param dewPoint the dewPoint to set
	 */
	public void setDewPoint(String dewPoint) {
		DewPoint = dewPoint;
	}

	/**
	 * @return the relativeHumidity
	 */
	public String getRelativeHumidity() {
		return RelativeHumidity;
	}

	/**
	 * @param relativeHumidity the relativeHumidity to set
	 */
	public void setRelativeHumidity(String relativeHumidity) {
		RelativeHumidity = relativeHumidity;
	}

	/**
	 * @return the status
	 */
	public String getStatus() {
		return Status;
	}

	/**
	 * @param status the status to set
	 */
	public void setStatus(String status) {
		Status = status;
	}
	
}
